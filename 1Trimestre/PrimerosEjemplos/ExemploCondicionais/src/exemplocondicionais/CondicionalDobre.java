package exemplocondicionais;

public class CondicionalDobre {
    public void verMaior(int num1, int num2) {
        if (num1 > num2) {
            System.out.println(num1 + " é maior que " + num2);
        } else {
            System.out.println(num2 + " é maior que " + num1);
        }
        
    }
}
